function addGlow(frame, glowRecipe) {
  var glow = figma.createEllipse();
  glow.name = 'PREVIEW GLOW';
  glow.x = glowRecipe.box.x;
  glow.y = glowRecipe.box.y;
  glow.resize(glowRecipe.box.width, glowRecipe.box.height);
  glow.fills = [{ type: 'SOLID', color: setPaintColor('d60d0d'), opacity: 0.37 }];
  glow.opacity = glowRecipe.opacity;
  glow.effects = [
    {
      type: 'LAYER_BLUR',
      radius: 233,
      visible: true,
      blendMode: 'NORMAL'
    }
  ];
  frame.appendChild(glow);
}

function cloneSourceNode(page, sourceFrameId, sourceNodeId) {
  var frame = findFrameById(page, sourceFrameId);
  var node = findNodeById(frame, sourceNodeId);
  if (!node) {
    throw new Error('Source node not found: ' + sourceNodeId);
  }
  return node.clone();
}

function placeClone(frame, clone, x, y, name) {
  clone.x = x;
  clone.y = y;
  if (name) {
    clone.name = name;
  }
  frame.appendChild(clone);
  return clone;
}

