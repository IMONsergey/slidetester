import { readFile, writeFile } from 'node:fs/promises';

const MATCHING_PATH = 'output/draft-to-source-matching.json';
const OUTPUT_PATH = 'output/figma-create-business-effect-v3.generated.js';

function getBusinessEffectEntry(matching) {
  const entry = matching.draftSlides.find((slide) => slide.draftRole === 'business-effect');
  if (!entry) {
    throw new Error('Missing business-effect entry in output/draft-to-source-matching.json.');
  }
  return entry;
}

function buildScript(entry) {
  const selected = entry.candidateFrames.find((candidate) => candidate.decision === 'selected');
  if (!selected) {
    throw new Error('Business effect matching does not contain a selected candidate.');
  }

  const candidateSummary = entry.candidateFrames.map((candidate) => ({
    frameId: candidate.frameId,
    frameName: candidate.frameName,
    decision: candidate.decision,
    reason: candidate.reason
  }));

  return `// Generated by src/figma/print-create-business-effect-v3-script.mjs
// Run this file through ChatGPT/Figma MCP use_figma or wrap it as a local Figma plugin.
const FILE_KEY = '735drrzVTmf4AhQJwQe6vX';
const TARGET_PAGE_ID = '0:1';
const TARGET_FRAME_NAME = 'PILOT V3 / 05 / Task Manager / Business Effect';
const SOURCE_FRAME_ID = '${selected.frameId}';
const ICONS_FRAME_ID = '25:11279';
const REQUIRED_FONT_ERROR = 'X5 Sans is required but not available.';
const PRODUCTION_MODE = '${entry.selectedProductionMode}';
const MATCHING_REPORT = ${JSON.stringify(candidateSummary, null, 2)};

const CONTENT = {
  title: 'Предполагаемый эффект',
  metrics: [
    {
      value: '+10–15%',
      label: 'Производительность труда',
      note: 'Рост эффективности рабочего времени команды'
    },
    {
      value: '-10%',
      label: 'ФОТ магазина от расходов',
      note: 'Снижение за счет роста производительности'
    },
    {
      value: '-0.4%',
      label: 'ФОТ магазина от выручки',
      note: 'Снижение за счет роста производительности'
    }
  ]
};

const REMOVED_CONTENT = [
  'old title text',
  'old KPI values',
  'old KPI labels',
  'old source note',
  'decorative arrow between old ambition cards',
  'old card icons that imply previous source meaning'
];

const page = figma.root.children.find(function(node) {
  return node.id === TARGET_PAGE_ID || node.name === 'Page 1';
});
if (!page) {
  throw new Error('Target page 0:1 / Page 1 not found.');
}

await figma.setCurrentPageAsync(page);

const availableFonts = await figma.listAvailableFontsAsync();
const x5Fonts = availableFonts.filter(function(entry) {
  return entry.fontName.family === 'X5 Sans';
});
if (x5Fonts.length === 0) {
  throw new Error(REQUIRED_FONT_ERROR);
}

const loadedFonts = {};

function fontKey(fontName) {
  return String(fontName.family) + '__' + String(fontName.style);
}

async function ensureFont(fontName) {
  if (!fontName || typeof fontName !== 'object' || fontName.family !== 'X5 Sans') {
    throw new Error(REQUIRED_FONT_ERROR);
  }
  const key = fontKey(fontName);
  if (!loadedFonts[key]) {
    await figma.loadFontAsync(fontName);
    loadedFonts[key] = true;
  }
}

function numberOr(value, fallback) {
  return typeof value === 'number' ? value : fallback;
}

function cleanText(value) {
  return String(value || '').replace(/\\s+/g, ' ').trim();
}

function getRightmostEdge(nodes) {
  return nodes.reduce(function(max, node) {
    return Math.max(max, numberOr(node.x, 0) + numberOr(node.width, 0));
  }, 0);
}

function collectTextNodes(root, bucket) {
  var target = bucket || [];
  if (root.type === 'TEXT') {
    target.push(root);
  }
  if ('children' in root && Array.isArray(root.children)) {
    root.children.forEach(function(child) {
      collectTextNodes(child, target);
    });
  }
  return target;
}

function removeNode(node, removedItems) {
  if (node) {
    removedItems.push({
      nodeId: node.id,
      nodeName: node.name,
      nodeType: node.type
    });
    node.remove();
  }
}

function getCardFrames(frame) {
  return frame.children
    .filter(function(node) {
      return node.type === 'FRAME' && numberOr(node.width, 0) >= 430 && numberOr(node.width, 0) <= 450 && numberOr(node.height, 0) >= 440;
    })
    .sort(function(left, right) {
      return numberOr(left.x, 0) - numberOr(right.x, 0);
    });
}

function findFirstTextNode(nodes) {
  var textNodes = nodes.filter(function(node) { return node.type === 'TEXT'; });
  return textNodes.length > 0 ? textNodes[0] : null;
}

function clonePaints(paints) {
  return Array.isArray(paints) ? JSON.parse(JSON.stringify(paints)) : [];
}

function cloneEffects(effects) {
  return Array.isArray(effects) ? JSON.parse(JSON.stringify(effects)) : [];
}

function createTextNode(text, templateNode, geometry, options) {
  var node = figma.createText();
  var fontName = options && options.fontName ? options.fontName : templateNode.fontName;
  var fontSize = options && typeof options.fontSize === 'number' ? options.fontSize : templateNode.fontSize;
  node.fontName = fontName;
  node.fontSize = fontSize;
  node.fills = clonePaints(templateNode.fills);
  node.effects = cloneEffects(templateNode.effects);
  node.opacity = numberOr(options && options.opacity, numberOr(templateNode.opacity, 1));
  node.textAlignHorizontal = options && options.textAlignHorizontal ? options.textAlignHorizontal : templateNode.textAlignHorizontal;
  node.textAlignVertical = options && options.textAlignVertical ? options.textAlignVertical : templateNode.textAlignVertical;
  node.lineHeight = options && options.lineHeight ? options.lineHeight : templateNode.lineHeight;
  node.letterSpacing = options && options.letterSpacing ? options.letterSpacing : templateNode.letterSpacing;
  node.characters = text;
  if (typeof geometry.width === 'number' && typeof geometry.height === 'number') {
    node.resize(geometry.width, geometry.height);
  }
  node.x = geometry.x;
  node.y = geometry.y;
  return node;
}

const sourceFrame = figma.currentPage.findOne(function(node) {
  return node.id === SOURCE_FRAME_ID;
});
if (!sourceFrame || sourceFrame.type !== 'FRAME') {
  throw new Error('Selected source frame not found: ' + SOURCE_FRAME_ID);
}

const rightmost = getRightmostEdge(
  figma.currentPage.children.filter(function(node) {
    return node.id !== ICONS_FRAME_ID;
  })
);

const duplicate = sourceFrame.clone();
duplicate.name = TARGET_FRAME_NAME;
duplicate.x = rightmost + 600;
duplicate.y = numberOr(sourceFrame.y, 0);
figma.currentPage.appendChild(duplicate);

const allTextNodes = collectTextNodes(duplicate);
const titleNode = allTextNodes
  .filter(function(node) { return numberOr(node.fontSize, 0) >= 90; })
  .sort(function(left, right) { return numberOr(right.fontSize, 0) - numberOr(left.fontSize, 0); })[0];
if (!titleNode) {
  throw new Error('Could not locate source title node in duplicated frame.');
}
if (titleNode.fontName === figma.mixed || titleNode.fontName.family !== 'X5 Sans') {
  throw new Error(REQUIRED_FONT_ERROR);
}
await ensureFont(titleNode.fontName);
titleNode.characters = CONTENT.title;

const cardFrames = getCardFrames(duplicate);
if (cardFrames.length !== 3) {
  throw new Error('Expected exactly three KPI card frames in duplicated source frame.');
}

const removedNodes = [];

duplicate.children
  .filter(function(node) {
    return node.type === 'TEXT' && cleanText(node.characters) === 'Источник: ТС5';
  })
  .forEach(function(node) {
    removeNode(node, removedNodes);
  });

duplicate.children
  .filter(function(node) {
    return node.type === 'GROUP' && cleanText(node.name).toLowerCase().indexOf('group') === 0;
  })
  .forEach(function(node) {
    var width = numberOr(node.width, 0);
    var height = numberOr(node.height, 0);
    if (width >= 300 && height >= 180) {
      removeNode(node, removedNodes);
    }
  });

const createdTextNodes = [];

for (var cardIndex = 0; cardIndex < cardFrames.length; cardIndex += 1) {
  var cardFrame = cardFrames[cardIndex];
  var metric = CONTENT.metrics[cardIndex];
  var cardTextNodes = collectTextNodes(cardFrame);
  var valueTemplate = cardTextNodes
    .filter(function(node) { return numberOr(node.fontSize, 0) >= 100; })
    .sort(function(left, right) { return numberOr(right.fontSize, 0) - numberOr(left.fontSize, 0); })[0];
  var labelTemplate = cardTextNodes
    .filter(function(node) { return numberOr(node.fontSize, 0) >= 30 && numberOr(node.fontSize, 0) <= 40; })
    .sort(function(left, right) { return numberOr(left.y, 0) - numberOr(right.y, 0); })[0];
  var noteTemplate = duplicate.children
    .filter(function(node) { return node.type === 'TEXT' && cleanText(node.characters) !== CONTENT.title; })
    .sort(function(left, right) { return numberOr(left.fontSize, 0) - numberOr(right.fontSize, 0); })[0];

  if (!valueTemplate || !labelTemplate || !noteTemplate) {
    throw new Error('Could not locate X5 Sans templates for card rebuild.');
  }

  if (valueTemplate.fontName === figma.mixed || labelTemplate.fontName === figma.mixed || noteTemplate.fontName === figma.mixed) {
    throw new Error(REQUIRED_FONT_ERROR);
  }

  await ensureFont(valueTemplate.fontName);
  await ensureFont(labelTemplate.fontName);
  await ensureFont(noteTemplate.fontName);

  cardFrame.children.slice().forEach(function(child) {
    removeNode(child, removedNodes);
  });

  var labelNode = createTextNode(metric.label, labelTemplate, {
    x: 32,
    y: 34,
    width: numberOr(cardFrame.width, 0) - 64,
    height: 90
  }, {
    fontName: labelTemplate.fontName,
    fontSize: numberOr(labelTemplate.fontSize, 36),
    textAlignHorizontal: 'LEFT',
    textAlignVertical: 'TOP'
  });

  var valueNode = createTextNode(metric.value, valueTemplate, {
    x: 32,
    y: 180,
    width: numberOr(cardFrame.width, 0) - 64,
    height: 110
  }, {
    fontName: valueTemplate.fontName,
    fontSize: numberOr(valueTemplate.fontSize, 118),
    textAlignHorizontal: 'LEFT',
    textAlignVertical: 'TOP'
  });

  var noteNode = createTextNode(metric.note, noteTemplate, {
    x: 32,
    y: 320,
    width: numberOr(cardFrame.width, 0) - 64,
    height: 82
  }, {
    fontName: noteTemplate.fontName,
    fontSize: 24,
    opacity: 0.9,
    lineHeight: { unit: 'PIXELS', value: 30 },
    textAlignHorizontal: 'LEFT',
    textAlignVertical: 'TOP'
  });

  cardFrame.appendChild(labelNode);
  cardFrame.appendChild(valueNode);
  cardFrame.appendChild(noteNode);

  createdTextNodes.push({
    cardIndex: cardIndex + 1,
    valueNodeId: valueNode.id,
    labelNodeId: labelNode.id,
    noteNodeId: noteNode.id
  });
}

return {
  createdFrames: [
    {
      createdFrameId: duplicate.id,
      createdFrameName: duplicate.name,
      sourceFrameId: sourceFrame.id,
      sourceFrameName: sourceFrame.name,
      selectedSourceCandidatesConsidered: MATCHING_REPORT,
      productionMode: PRODUCTION_MODE,
      x: duplicate.x,
      y: duplicate.y,
      selectedSourceFrames: [SOURCE_FRAME_ID],
      rejectedDirectCloneFrameId: '25:7247',
      rejectedDirectCloneReason: 'Wrong semantic architecture: market-comparison charts and legend do not match a self-contained KPI summary.',
      elementsReused: ['background', 'card shells', 'title position', 'glows', 'X5 Sans styles'],
      elementsRemoved: REMOVED_CONTENT,
      elementsNewlyCreated: ['new title text', 'three KPI value texts', 'three KPI label texts', 'three KPI note texts'],
      removedNodes: removedNodes,
      createdTextNodes: createdTextNodes,
      visualRisks: [
        'Card note copy may need line-break tuning after screenshot review.',
        'The source card shell glow balance should be checked against final text density in Figma Desktop.'
      ]
    }
  ],
  x5SansAvailable: true,
  originalFramesModified: false,
  iconsFrameModified: false
};`;
}

async function main() {
  const matching = JSON.parse(await readFile(MATCHING_PATH, 'utf8'));
  const entry = getBusinessEffectEntry(matching);
  await writeFile(OUTPUT_PATH, `${buildScript(entry)}\n`, 'utf8');
  console.log(`Wrote ${OUTPUT_PATH}.`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
