import { mkdir, readFile, writeFile } from 'node:fs/promises';

const SOURCE_SCRIPT = 'output/figma-create-pilot-v2.generated.js';
const PLUGIN_DIR = 'figma-plugin';
const MANIFEST_PATH = `${PLUGIN_DIR}/manifest.json`;
const CODE_PATH = `${PLUGIN_DIR}/code.js`;
const README_PATH = `${PLUGIN_DIR}/README.md`;

function buildManifest() {
  return {
    name: 'Slidetester Pilot V2 Local Runner',
    api: '1.0.0',
    editorType: ['figma'],
    main: 'code.js',
    documentAccess: 'dynamic-page',
    networkAccess: {
      allowedDomains: ['none']
    }
  };
}

function replaceFinalReturn(source) {
  const marker = 'return {';
  const returnIndex = source.lastIndexOf(marker);
  if (returnIndex === -1) {
    throw new Error('Could not find final return statement in output/figma-create-pilot-v2.generated.js');
  }

  const bodyBeforeReturn = source.slice(0, returnIndex);
  const reportExpression = source.slice(returnIndex + 'return '.length).trim().replace(/;?\s*$/, '');

  return `${bodyBeforeReturn}const __pluginReport = ${reportExpression};
console.log(JSON.stringify(__pluginReport, null, 2));
figma.closePlugin('Pilot V2 clone/adapt completed. Open Plugins → Development → Open console for the replacement report.');`;
}

function buildPluginCode(source) {
  const transformed = replaceFinalReturn(source);

  return `// Generated by src/figma/build-local-plugin.mjs from output/figma-create-pilot-v2.generated.js
async function runPlugin() {
${transformed}
}

runPlugin().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(error);
  if (message === 'X5 Sans is required but not available.' || message === 'X5 Sans is required but not available in this Figma environment.') {
    figma.closePlugin('X5 Sans is required but not available in this Figma environment.');
    return;
  }
  figma.closePlugin(\`Plugin failed: \${message}\`);
});
`;
}

function buildReadme() {
  return `# Local Figma Plugin Runner

This plugin runs the Pilot V2 clone/adapt flow directly inside Figma Desktop, using the local Figma environment where X5 Sans is available.

## What it does

- duplicates source frame \`25:6981\` into \`PILOT V2 / 01 / Task Manager / Cover\`
- duplicates source frame \`25:7247\` into \`PILOT V2 / 05 / Task Manager / Business Effect\`
- preserves source geometry, vectors, masks, effects, glows, images, and typography
- replaces only selected text nodes
- enforces X5 Sans without Inter fallback
- places duplicates in a new column to the right of existing frames

## Figma Desktop

Plugins → Development → Import plugin from manifest
Select \`figma-plugin/manifest.json\`
Open the target Figma file
Run the plugin

## Notes

- The plugin does not modify original source frames.
- The plugin does not modify the Icons frame.
- If X5 Sans is missing in the local Figma runtime, the plugin stops with:
  \`X5 Sans is required but not available in this Figma environment.\`
- The replacement report is written to the plugin console.
`;
}

async function main() {
  await mkdir(PLUGIN_DIR, { recursive: true });

  const source = await readFile(SOURCE_SCRIPT, 'utf8');
  const manifest = buildManifest();
  const code = buildPluginCode(source);
  const readme = buildReadme();

  await writeFile(MANIFEST_PATH, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
  await writeFile(CODE_PATH, code, 'utf8');
  await writeFile(README_PATH, readme, 'utf8');

  console.log(`Wrote ${MANIFEST_PATH}`);
  console.log(`Wrote ${CODE_PATH}`);
  console.log(`Wrote ${README_PATH}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
