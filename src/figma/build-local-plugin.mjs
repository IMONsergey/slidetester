import { mkdir, readFile, writeFile } from 'node:fs/promises';

const SOURCE_SCRIPT = 'output/figma-create-business-effect-v3.generated.js';
const PLUGIN_DIR = 'figma-plugin';
const MANIFEST_PATH = `${PLUGIN_DIR}/manifest.json`;
const CODE_PATH = `${PLUGIN_DIR}/code.js`;
const README_PATH = `${PLUGIN_DIR}/README.md`;

function buildManifest() {
  return {
    name: 'Slidetester Business Effect V3 Local Runner',
    api: '1.0.0',
    editorType: ['figma'],
    main: 'code.js',
    documentAccess: 'dynamic-page',
    networkAccess: {
      allowedDomains: ['none']
    }
  };
}

function replaceFinalReturn(source) {
  const marker = 'return {';
  const returnIndex = source.lastIndexOf(marker);
  if (returnIndex === -1) {
    throw new Error('Could not find final return statement in output/figma-create-business-effect-v3.generated.js');
  }

  const bodyBeforeReturn = source.slice(0, returnIndex);
  const reportExpression = source.slice(returnIndex + 'return '.length).trim().replace(/;?\s*$/, '');

  return `${bodyBeforeReturn}return ${reportExpression};`;
}

function buildPluginCode(source) {
  const transformed = replaceFinalReturn(source);

  return `// Generated by src/figma/build-local-plugin.mjs from output/figma-create-business-effect-v3.generated.js
async function main() {
${transformed}
}

main()
  .then(function(result) {
    console.log('[Slidetester Business Effect V3] completed', result);
    figma.notify('Slidetester Business Effect V3 completed');
    figma.closePlugin('Business Effect V3 build completed. Open Plugins -> Development -> Open console for the replacement report.');
  })
  .catch(function(error) {
    var rawMessage = error && error.message ? error.message : String(error);
    var message = rawMessage === 'X5 Sans is required but not available.'
      ? 'X5 Sans is required but not available in this Figma environment.'
      : rawMessage;

    console.error('[Slidetester Business Effect V3] failed', error);
    figma.notify('Slidetester Business Effect V3 failed: ' + message, { error: true });
    figma.closePlugin(message);
  });
`;
}

function buildReadme() {
  return `# Local Figma Plugin Runner

This plugin runs the Business Effect V3 rebuild directly inside Figma Desktop, using the local Figma environment where X5 Sans is available.

## What it does

- duplicates source frame \`25:7044\` into \`PILOT V3 / 05 / Task Manager / Business Effect\`
- preserves source background, card shells, glows, layout geometry, and X5 Sans typography language
- removes unrelated legacy KPI content from the cloned frame
- rebuilds only the required title and three metric blocks
- enforces X5 Sans without Inter fallback
- places the duplicate in a new column to the right of existing frames

## Figma Desktop

Plugins → Development → Import plugin from manifest
Select \`figma-plugin/manifest.json\`
Open the target Figma file
Run the plugin

## Notes

- The plugin does not modify original source frames.
- The plugin does not modify the Icons frame.
- If X5 Sans is missing in the local Figma runtime, the plugin stops with:
  \`X5 Sans is required but not available in this Figma environment.\`
- The replacement report is written to the plugin console.
`;
}

async function main() {
  await mkdir(PLUGIN_DIR, { recursive: true });

  const source = await readFile(SOURCE_SCRIPT, 'utf8');
  const manifest = buildManifest();
  const code = buildPluginCode(source);
  const readme = buildReadme();

  await writeFile(MANIFEST_PATH, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
  await writeFile(CODE_PATH, code, 'utf8');
  await writeFile(README_PATH, readme, 'utf8');

  console.log(`Wrote ${MANIFEST_PATH}`);
  console.log(`Wrote ${CODE_PATH}`);
  console.log(`Wrote ${README_PATH}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
